#include <iostream>
#include <iomanip>
using namespace std;
int main ()
{
    cout << oct;
    cout << "11 in oct: " << 11 << endl;
    cout << dec;
    cout << "11 in dec: " << 11 << endl;
    cout << hex;
    cout << "11 in hex: " << 11 << endl;
    cout << oct;
    cout << "011 in oct: " << 011 << endl;
    cout << dec;
    cout << "011 in dec: " << 011 << endl;
    cout << hex;
    cout << "011 in hex: " << 011 << endl;
    cout << oct;
    cout << "0x11 in oct: " << 0x11 << endl;
    cout << dec;
    cout << "0x11 in dec: " << 0x11 << endl;
    cout << hex;
    cout << "0x11 in hex: " << 0x11 << endl;
    return 0;
} // main

